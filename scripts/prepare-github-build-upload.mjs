import { copyFileSync, cpSync, existsSync, mkdirSync, rmSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const projectRoot = resolve(__dirname, '..');
const releaseRoot = join(projectRoot, 'release', 'github-build-upload');
const distPath = join(projectRoot, 'dist');
const nodeModulesPath = join(projectRoot, 'node_modules');

const includePaths = [
  'dist',
  'package.json',
  'package-lock.json',
  'vite.config.ts',
  'index.html',
  'README.md',
];

function ensureDependencyTree() {
  if (existsSync(nodeModulesPath)) return;
  console.error('[github-build-upload] Missing node_modules. Run `npm ci` first, then rerun this script.');
  process.exit(1);
}

function runBuild() {
  const result = spawnSync('npm', ['run', 'build'], {
    cwd: projectRoot,
    stdio: 'inherit',
    shell: process.platform === 'win32',
    env: process.env,
  });

  if (result.error) {
    throw result.error;
  }

  if (typeof result.status === 'number' && result.status !== 0) {
    throw new Error(`[github-build-upload] Build failed with exit code ${result.status}`);
  }
}

function copyIncludedPaths() {
  rmSync(releaseRoot, { recursive: true, force: true });
  mkdirSync(releaseRoot, { recursive: true });

  for (const relativePath of includePaths) {
    const sourcePath = join(projectRoot, relativePath);
    const destinationPath = join(releaseRoot, relativePath);
    if (!existsSync(sourcePath)) continue;
    const stats = statSync(sourcePath);
    if (stats.isDirectory()) {
      cpSync(sourcePath, destinationPath, { recursive: true });
    } else {
      mkdirSync(dirname(destinationPath), { recursive: true });
      copyFileSync(sourcePath, destinationPath);
    }
  }
}

function writeManifest() {
  const manifest = {
    artifact: 'github-build-upload',
    preparedAt: new Date().toISOString(),
    projectRoot,
    buildDirectory: 'dist',
    releaseDirectory: 'release/github-build-upload',
    includedPaths,
    notes: [
      'dist is ignored by default in .gitignore, so use git add -f dist when committing the build.',
      'This folder is intended as a clean handoff package for pushing a built artifact to GitHub.',
    ],
    nextSteps: [
      'git add -A',
      'git add -f dist',
      'git commit -m "Add latest production build"',
      'git push origin main',
    ],
  };

  writeFileSync(
    join(releaseRoot, 'github-build-upload-manifest.json'),
    `${JSON.stringify(manifest, null, 2)}\n`,
    'utf8',
  );
}

function writeGuide() {
  const guide = `# GitHub Build Upload

This folder was generated by \`scripts/prepare-github-build-upload.mjs\`.

Included:
- \`dist/\`
- \`package.json\`
- \`package-lock.json\`
- \`vite.config.ts\`
- \`index.html\`
- \`README.md\`

Git commands to upload the build to GitHub:

\`\`\`bash
git add -A
git add -f dist
git commit -m "Add latest production build"
git push origin main
\`\`\`

Important:
- \`dist/\` is ignored in \`.gitignore\`, so it must be force-added.
- If you only want source code on GitHub, skip \`git add -f dist\`.
`;

  writeFileSync(join(releaseRoot, 'UPLOAD_TO_GITHUB.md'), guide, 'utf8');
}

ensureDependencyTree();
runBuild();

if (!existsSync(distPath)) {
  console.error('[github-build-upload] Build completed but dist/ was not generated.');
  process.exit(1);
}

copyIncludedPaths();
writeManifest();
writeGuide();

console.log(`[github-build-upload] Build prepared at ${releaseRoot}`);
console.log('[github-build-upload] Next commands:');
console.log('  git add -A');
console.log('  git add -f dist');
console.log('  git commit -m "Add latest production build"');
console.log('  git push origin main');
